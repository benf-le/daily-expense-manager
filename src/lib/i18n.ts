export type TranslationKeys = typeof translations.vi;

export function getTranslations(locale: Locale): TranslationKeys {
  return translations[locale] as TranslationKeys;
}

export function formatCurrency(amount: number, locale: Locale = 'vi'): string {
  const numberFormatLocale = {
    vi: 'vi-VN',
    en: 'en-US',
    hi: 'en-IN',
    ko: 'ko-KR',
    zh: 'zh-CN',
  }[locale] || 'vi-VN';

  return new Intl.NumberFormat(numberFormatLocale, {
    style: 'currency',
    currency: 'VND',
    maximumFractionDigits: 0,
  }).format(amount);
}

export function formatDate(date: string | Date, locale: Locale = 'vi'): string {
  const d = new Date(date);
  switch (locale) {
    case 'vi':
      return d.toLocaleDateString('vi-VN');
    case 'en':
      return d.toLocaleDateString('en-US');
    case 'hi':
      return d.toLocaleDateString('en-IN'); // Hindi usually uses DD/MM/YYYY
    case 'ko':
      return d.toLocaleDateString('ko-KR');
    case 'zh':
      return d.toLocaleDateString('zh-CN');
    default:
      return d.toLocaleDateString();
  }
}
